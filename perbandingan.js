let x = 10;
let z = 11;

console.log(x > z); // Lebih Dari
console.log(x < z); // Kurang Dari
console.log(x >= z); // Lebih Dari Sama Dengan
console.log(x <= z); // Kurang Dari Sama Dengan
console.log(x == z); // Sama Dengan (Tidak Identik Tipe Data)
console.log(x === z); // Sama Dengan (Identik Tipe Data)
console.log(x != z); // Tidak Sama Dengan (Tidak Identik Tipe)
console.log(x !== z); // Tidak Sama Dengan (Identik Tipe Data)
